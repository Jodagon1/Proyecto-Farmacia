    void Call(String s,String s1,Usuario us) {
        this.us = us;
        this.s = s;
        Aceptar.setIcon(new ImageIcon(this.getClass().getResource("/Imagen/Ok.png")));
        Clear.setIcon(new ImageIcon(this.getClass().getResource("/Imagen/Clean.png")));
        Exit.setIcon(new ImageIcon(this.getClass().getResource("/Imagen/exit.png")));
        jCheckBox1.setSelected(true);
        bg.add(jCheckBox1);
        bg.add(jCheckBox2);
        Text.setText(s);       
        this.setTitle(s1);
        if(us != null){
            if(s1.equals("Modificar")){
                jTextField1.setText(us.getName());
                jPasswordField1.setText(us.getPassword());
                if(us.isAdministrador()){
                    jCheckBox1.setSelected(true);
                }else{
                    jCheckBox2.setSelected(true);
                }
            }
        }
        this.setVisible(true);
    }

    private void Accion() {
        if(!(s.equals("Modificar Usuario"))){
            int cont = -1;
            List<Usuario> s = new Conectar().ConexionUs();
            if(!(jTextField1.getText().equals("") && jPasswordField1.getPassword().equals("") && s.isEmpty())){ 
                Usuario p = new Usuario();
                p.setName(jTextField1.getText());
                p.setPassword(new String(jPasswordField1.getPassword()));
                if(jCheckBox1.isSelected()){
                    p.setAdministrador(true);
                    p.setEmpleado(false);
                }else{
                    p.setAdministrador(false);
                    p.setEmpleado(true);
                }                
                new Conectar().InsertarUs(p);
                jTextField1.setText("");
                jPasswordField1.setText("");
            }else if(!(jTextField1.getText().equals("") && jPasswordField1.getPassword().equals("") && (!s.isEmpty()))){
                cont = 0;
                for(Usuario us: s){
                    Usuario p = new Usuario();
                    p.setId(us.getId());
                    p.setName(jTextField1.getText());
                    p.setPassword(new String(jPasswordField1.getPassword()));
                    if(jCheckBox1.isSelected()){
                        p.setAdministrador(true);
                        p.setEmpleado(false);
                    }else{
                        p.setAdministrador(false);
                        p.setEmpleado(true);
                    }
                    if(p.getName().equalsIgnoreCase(us.getName())){
                        cont++;                    
                    }else{
                        new Conectar().InsertarUs(p);
                        jTextField1.setText("");
                        jPasswordField1.setText("");
                    }
                }
                
            }else if(!(jTextField1.getText().equals("") && jPasswordField1.getPassword().equals(""))){
                JOptionPane.showMessageDialog(null,"Debe de llenar los campos1");
            }
            if(cont == s.size()){
                JOptionPane.showMessageDialog(null, "Usuario Existente");
            }
        }else if(s.equals("Modificar Usuario")){
            if(!((jTextField1.getText().equals("")) && (jPasswordField1.getPassword().equals("")))){
                Usuario s = new Usuario();
                if(us != null){
                    s.setId(us.getId());
                }
                s.setName(jTextField1.getText());
                s.setPassword(new String(jPasswordField1.getPassword()));
                if(jCheckBox1.isSelected()){
                    s.setAdministrador(true);
                    s.setEmpleado(false);
                }else{
                    s.setAdministrador(false);
                    s.setEmpleado(true);
                }
                new Conectar().ModificarUs(s);
            }else{
                JOptionPane.showMessageDialog(null, "Debe de llenar los campos");
            }
        }
    }
