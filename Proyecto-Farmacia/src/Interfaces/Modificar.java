    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        productos = new ArrayList<>();
        int cont = 0;
        List<Producto> prod = new ArrayList<>();
        prod = new Conectar().ConexionProd();
        String s = Campo.getText();
        String id;
        dlm = new DefaultListModel();
        boolean b = false;
        for(Producto p : prod){
            Producto pd = new Producto();
            pd = new Comparar().CompararAll(p, s);
            if(pd == null){
                cont++;
            }else{
                String s1 = "Id: "+(id = new Search().SetLength(""+pd.getId()))+", Nombre: "+pd.getName()+","
                        + " Marca: "+pd.getMarca();
                productos.add(pd);                
                dlm.addElement(s1);
                Lista.setModel(dlm);
            }
            if(cont == prod.size()){
                b = true;
            }
        }
        if(b){
            for(Producto p:prod){
                if(p.getName().equals(Campo.getText())){
                    productos.add(p);
                    dlm = new DefaultListModel();
                    String s1 = "Id: "+(id = new Search().SetLength(""+p.getId()))+", Nombre: "+p.getName()+","
                        + " Marca: "+p.getMarca();
                    dlm.addElement(s1);
                    Lista.setModel(dlm);
                }
            }
        }
    }//GEN-LAST:event_SearchActionPerformed
