    public String SetLength(String x){
        String y = null;
        if(x.length()==1){
            y = "00000"+x;
        }else if(x.length()==2){
            y = "0000"+x;
        }else if(x.length()==3){
            y = "000"+x;
        }else if(x.length()==4){
            y = "00"+x;
        }else if(x.length()==5){
            y = "0"+x;
        }else if(x.length()==6){
            y = ""+x;
        }          
        return y;
    }
